
public class OperacoesRelacionais {

	public static void main(String[] args) {
		
		int nota = 20;
		int nota2 = 10;
		
		if (nota == nota2) {
			System.out.println("Igual");
		}
		
		if (nota != nota2) {
			System.out.println("Diferente");
		}
		
		if (nota > nota2) {
			System.out.println("Nota é menor que a nota 2");
		}
		
		if (nota < nota2) {
			System.out.println("Nota2 é maior que a nota");
		}
	}
}
